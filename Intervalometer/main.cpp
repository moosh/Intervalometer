#include "Prefix.h"
#include "IVModel.h"
#include "IVView.h"
#include "IVController.h"

/******************************************************************************

******************************************************************************/
IVModel 		mModel;
IVView			mView;
IVController	mController;

/******************************************************************************

******************************************************************************/
int main(void)
{
	init();
	
	pinMode(kPinLeftButton, INPUT);
	pinMode(kPinRightButton, INPUT);
	pinMode(kPinUpButton, INPUT);
	pinMode(kPinDownButton, INPUT);
	pinMode(kPinEnterButton, INPUT);
	pinMode(kPinShutterTrigger, OUTPUT);
	
	randomSeed(analogRead(5));

	mModel.Init();
	mView.Init(&mModel);
	mController.Init(&mModel, &mView);

    for (;;)
	{
		mController.Timeslice();
	}
    
    return 0;
}

